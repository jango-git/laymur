/** Input events emitted by UI elements */
export enum UIInputEvent {
  /** Pointer pressed */
  PRESSED = "pointerdown",
  /** Pointer moved */
  MOVED = "pointermove",
  /** Pointer released */
  RELEASED = "pointerup",
  /** Pointer entered element */
  ENTERED = "pointerenter",
  /** Pointer left element */
  LEFT = "pointerleave",
}
